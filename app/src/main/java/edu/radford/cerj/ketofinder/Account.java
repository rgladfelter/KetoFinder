    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParam1 = getArguments().getString(ARG_PARAM1);
            mParam2 = getArguments().getString(ARG_PARAM2);
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View view = inflater.inflate(R.layout.fragment_account, container, false);

        FirebaseUser user = mAuth.getCurrentUser();
        mDatabase.child("Users").child(user.getUid());

        //These long winded calls make sure that if there is a user already created that they will
        //get their respective names, about texts, and profile pictures if applicable
        name = view.findViewById(R.id.users_name);
        mDatabase.child("Users").child(user.getUid()).child("Name").addValueEventListener(new ValueEventListener() {
            @Override
            public void onDataChange(DataSnapshot dataSnapshot) {
                if(dataSnapshot.getValue() != null){
                    name.setText((CharSequence) dataSnapshot.getValue());
                }
            }

            @Override
            public void onCancelled(DatabaseError databaseError) {
                System.out.println("The read failed: " + databaseError.getCode());
            }
        });

        about = view.findViewById(R.id.about_text);
        mDatabase.child("Users").child(user.getUid()).child("About").addValueEventListener(new ValueEventListener() {
            @Override
            public void onDataChange(DataSnapshot dataSnapshot) {
                if(dataSnapshot.getValue() != null){
                    about.setText((CharSequence) dataSnapshot.getValue());
                }
            }

            @Override
            public void onCancelled(DatabaseError databaseError) {
                System.out.println("The read failed: " + databaseError.getCode());
            }
        });

        pic = view.findViewById(R.id.profile_pic);
        mDatabase.child("Users").child(user.getUid()).child("Picture").addValueEventListener(new ValueEventListener() {
            @Override
            public void onDataChange(DataSnapshot dataSnapshot) {
                if(dataSnapshot.getValue() != null){
                    new DownloadImageTask(pic).execute((String) dataSnapshot.getValue());
                }
            }

            @Override
            public void onCancelled(DatabaseError databaseError) {
                System.out.println("The read failed: " + databaseError.getCode());
            }
        });
        if(getArguments().getString("profile_pic") != null)
            new DownloadImageTask(pic).execute(getArguments().getString("profile_pic"));

        return view;
    }

    // TODO: Rename method, update argument and hook method into UI event
